// Variables: Think of variables as empty containers to store values.

// var : The var keyword is used to declare a variable. This is a old way to declare a variable.
var a = 1;
var b = 2;
var c = a + b;
console.log(c);

// const : The const keyword is used to declare a constant variable. This value cannot be changed. This is a new way to declare a variable.
const d = 1;
const e = 2;
const f = d + e;
console.log(f);

// let : The let keyword is used to declare a variable. This value can be changed. This is a new way to declare a variable.
let g = 1;
let h = 2;
let i = g + h;
console.log(i);
